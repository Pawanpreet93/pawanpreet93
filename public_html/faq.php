                    </p>
                </div>

                <h3>What happens to the excess power?</h3>
                <div>
                    <p>
                        In the case of the plant producing more power than is needed by the house – the excess is sent to the grid. This is calculated by a NET meter that measures power in and power out. At the end of the month you will see the adjustment in your bill. Excess power credits are carried forward to the next month’s billing to offset it. This is a good way to reduce your bill even when you don’t consume. Next time you leave the house for a vacation or work – your plant is making you money!
                    </p>
                </div>

                <h3>How does solar power work at night &amp; when I am not at home?</h3>
                <div>
                    <p>
                        Well to be blunt about this – it doesn’t work at night! The plant simply rests and waits for the next rays of sun. Now should you be out of the house – on vacation, work etc.; the plant produces power and sends the excess to the grid. This is measured via the NET meter as explained above. The final bill generated is the power consumed minus the power sent to the grid. In case you sent more to the grid – then it is carried forward to the next month.
                    </p>
                </div>

                <h3>I already have generators and inverters – how does this work now?</h3>
                <div>
                    <p>
                        The solar plant on your rooftop will be an independent energy source. It will work in parallel with the electricity coming from the grid and any other backup source that you may have. You do not have to get rid of any of the inverters or generators that you have already bought or connected – as the solar plant connects separately. It is also configured in a way to minimize the use of the generator in the case of a power-cut. No need for expensive fuel or cumbersome batteries.
